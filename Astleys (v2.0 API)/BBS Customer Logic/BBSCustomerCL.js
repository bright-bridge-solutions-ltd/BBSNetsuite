    /**
     * Validation function to be executed when field is changed.
     *
     * @param {Object} scriptContext
     * @param {Record} scriptContext.currentRecord - Current form record
     * @param {string} scriptContext.sublistId - Sublist name
     * @param {string} scriptContext.fieldId - Field name
     * @param {number} scriptContext.lineNum - Line number. Will be undefined if not a sublist or matrix field
     * @param {number} scriptContext.columnNum - Line number. Will be undefined if not a matrix field
     *
     * @returns {boolean} Return true if field is valid
     *
     * @since 2015.2
     */
    function validateField(scriptContext) {
    	
    	// if the custentity_bbs_customer_sales_note has been changed
    	if (scriptContext.fieldId == 'custentity_bbs_customer_sales_note')
    		{
    			// get the length of the custentity_bbs_customer_sales_note field
    			var fieldLength = scriptContext.currentRecord.getValue({
    				fieldId: 'custentity_bbs_customer_sales_note'
    			}).length;
    			
    			// if fieldLength > 250
    			if (fieldLength > 250)
    				{
    					// display an error message
    					dialog.alert({
    						title: '⚠️ Error',
    						message: 'You have exceeded the maximum length for the Customer Document Note field of <b>250</b> characters.<br><br>Please amend this field before saving the record.'
    					});
    				}
    		}
    	else
    		{
    			return true;
    		}

    }

